package class3;

public class E16RelationalOperator {
    public static void main(String[] args) {
        int num1=10;
        int num2=20;
        System.out.println(num1>num2);//false
        System.out.println(num1<num2);//true
        System.out.println(num1==num2);//false
        System.out.println(num1!=num2);//true
        System.out.println(num1>=num2);//false
        System.out.println(num1<=num2);//true
    }

}
