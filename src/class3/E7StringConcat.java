package class3;

public class E7StringConcat {
    public static void main(String[] args) {
        String firstName="Jamil";
        String lastName="Gulab";
        String fullName=firstName+lastName;
        System.out.println(fullName);
        // only + allowed in java for String
        String gul=firstName+10;
        System.out.println(gul);

    }
}
