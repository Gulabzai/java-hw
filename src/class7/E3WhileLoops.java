package class7;

public class E3WhileLoops {
    public static void main(String[] args) {

        //we want to print "hello world five times on the console
        int counter=1;
        //this condition will return true five times

        while (counter<=5){
            System.out.println("hello world");
            //in every iteration the value of the counter variable will increase
            counter++;
        }
    }
}
